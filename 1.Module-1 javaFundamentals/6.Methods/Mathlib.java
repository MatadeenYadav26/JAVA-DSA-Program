package class6_Methods;

public class Mathlib {
    static void main(String[] args) {
        int a = 5;
        int b = 10;

        System.out.println(Math.min(a,b));
        System.out.println(Math.max(a,b));
        //sqrt
        System.out.println(Math.sqrt(16));
        System.out.println(Math.pow(2,3)); // power
        System.out.println(Math.abs(-3)); //gives absolute value
        System.out.println(Math.random()*10);// returns random value from 0 to 1.
        System.out.println(Math.floor(5.9));// just chota int return karega
        System.out.println(Math.ceil(5.1));// just bada int return karega
        System.out.println(Math.round(5.4));//roundoff
        System.out.println(Math.round(5.6));//roundoff


    }





}
